/*
 * Exercício 1: Contagem de 1 a 10
Objetivo: Usar um loop while para imprimir os números de 1 a 10.
 * */

package ExerciciosFor;

public class Contar1a10 {
    public static void main(String[] args) {
        int contador = 1;
        while (contador <= 10) {
            System.out.print(contador+" ");
            contador++;
        }
    }
}
