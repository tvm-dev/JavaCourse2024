package javaCurso2024;

public class BVariablesFloat {
	public static void main(String[] args) {
		
		//=====
		double price = 30;
		System.out.println("The price is US$ " + price + " Dollars.");
		
		//=====
		double account = 3.5 / 2;
		System.out.println(account);
		//=====
		int account2 = 3 / 2; // result is Int
		System.out.println(account2);
		//=====
		double account3 = 3.0 / 2; //result is Float
		System.out.println(account3);
	
		
	}

}
